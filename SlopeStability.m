x(1) = str2double(get(handles.edit1,'String'));
y(1) = str2double(get(handles.edit2,'String'));

x(2) = str2double(get(handles.edit3,'String'));
y(2) = str2double(get(handles.edit4,'String'));

x(3) = str2double(get(handles.edit5,'String'));
y(3) = str2double(get(handles.edit6,'String'));

x(4) = str2double(get(handles.edit7,'String'));
y(4) = str2double(get(handles.edit8,'String'));

%Put slope points in two matrixs
xs = [x(1) x(2) x(3) x(4)];
ys = [y(1) y(2) y(3) y(4)];

%Soil properties
c = str2double(get(handles.edit140,'String'));
phi = str2double(get(handles.edit26,'String'));
gamma = str2double(get(handles.edit27,'String'));
ns = str2double(get(handles.edit41,'String'));
uerror = str2double(get(handles.edit48,'String'));


%------------------------------------------------------


hold on
grid on
axis equal
k=1;
for i=xc(1):1:xc(2)
    for j=yc(1):1:yc(2)
       
r=zeros(1,4);
    r(1)=sqrt((i-x(1))^2+(j-y(1))^2);
    r(2)=sqrt((i-x(2))^2+(j-y(2))^2);
    r(3)=sqrt((i-x(3))^2+(j-y(3))^2);
    r(4)=sqrt((i-x(4))^2+(j-y(4))^2);
   
rmin=min(r);
rmax=max(r);

        for rd=rmin:0.5:rmax


        [xintl, xintr] = intersectionpoints(xs,ys,i,j,rd); % find the intersections between the slope and circles


        [weight, bn, ln, an, andeg] = slicepropertiy(xs,ys,i, ...
            j,rd,ns,xintl, xintr,gamma);

if (get(handles.radiobutton1,'Value')==get(handles.radiobutton1,'Max'))
[fs] = bishopmethod(c, phi, bn, weight, an,uerror);

set(handles.text32,'String',['Note: The selected Method is: Bishop']);

elseif (get(handles.radiobutton2,'Value')==get(handles.radiobutton2,'Max'))
[fs] = ordinarymethod(c, phi, ln, weight, an);

set(handles.text32,'String',['Note: The selected Method is: Ordinary' ...
        ' Method of Slices']);

end

s(k,1)=i;
s(k,2)=j;
s(k,3)=rd;
s(k,4)=fs;

k=k+1;

        end
    end
end
s;
handles.storageMatrix = s; %store data then I can call it
lowest_fs=min(s(:,4));
set(handles.edit47, 'String', num2str(lowest_fs));
row = find(s(:,4) == lowest_fs);
viscircles([s(row,1),s(row,2)],s(row,3), 'color', 'r');
clc
guidata(hObject, handles)





