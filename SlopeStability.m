function pushbutton5_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

xc(1) = str2double(get(handles.edit29,'String'));
yc(1) = str2double(get(handles.edit30,'String'));

xc(2) = str2double(get(handles.edit19,'String'));
yc(2) = str2double(get(handles.edit28,'String'));

%Slope Points
x(1) = str2double(get(handles.edit1,'String'));
y(1) = str2double(get(handles.edit2,'String'));

x(2) = str2double(get(handles.edit3,'String'));
y(2) = str2double(get(handles.edit4,'String'));

x(3) = str2double(get(handles.edit5,'String'));
y(3) = str2double(get(handles.edit6,'String'));

x(4) = str2double(get(handles.edit7,'String'));
y(4) = str2double(get(handles.edit8,'String'));

%Put slope points in two matrixs
xs = [x(1) x(2) x(3) x(4)];
ys = [y(1) y(2) y(3) y(4)];


%------------------------------------------------------


hold on
grid on
axis equal
k=1;
for i=xc(1):1:xc(2)
    for j=yc(1):1:yc(2)
       
r=zeros(1,4);
    r(1)=sqrt((i-x(1))^2+(j-y(1))^2);
    r(2)=sqrt((i-x(2))^2+(j-y(2))^2);
    r(3)=sqrt((i-x(3))^2+(j-y(3))^2);
    r(4)=sqrt((i-x(4))^2+(j-y(4))^2);
   
rmin=min(r);
rmax=max(r);

        for rd=rmin:0.5:rmax

         viscircles([i,j],rd, 'color', 'b');
         
        end
    end
end

